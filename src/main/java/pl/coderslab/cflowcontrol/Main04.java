package pl.coderslab.cflowcontrol;

public class Main04 {

	public static void main(String[] args) {

		// pętla for
		for (int i = 1; i <= 10; i++) {
			System.out.print(i + " ");
		}

		System.out.println();

		// pętla while
		int j = 1;
		while (j <= 10) {
			System.out.print(j + " ");
			j++;
		}
	}

	}

